package com.earnings.payadjustements.entity;

public class Component {
	private Integer componentId;
    private Integer componentValue;
	/**
	 * @return the componentId
	 */
	public Integer getComponentId() {
		return componentId;
	}
	/**
	 * @param componentId the componentId to set
	 */
	public void setComponentId(Integer componentId) {
		this.componentId = componentId;
	}
	/**
	 * @return the componentValue
	 */
	public Integer getComponentValue() {
		return componentValue;
	}
	/**
	 * @param componentValue the componentValue to set
	 */
	public void setComponentValue(Integer componentValue) {
		this.componentValue = componentValue;
	}
    
}
